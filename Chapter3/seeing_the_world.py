# List 5 places I would like to go
places = ["Italy", "France","Japan","Jamaica","Greece"]

# Print the list above
print(places)

# Print non-permanent sorted list
print(sorted(places))
# Print to make sure the sort was not permanent
print(places)

#Reverse and print list 
places.reverse()
print(places)

# Reverse and print list again
places.reverse()
print(places)

# Permanent sort of list alphabetically and print
places.sort()
print(places)

# Reverse list that was alphabetically sorted and print
places.reverse()
print(places)


